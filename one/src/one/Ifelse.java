package one;

public class Ifelse {
	public static void main(String[] args)
	{

		int one=10;
		int two=20;
		if(one==two)
			System.out.println("one equal to two");
		else if(one>=two)
			System.out.println("one greater than two");
		else
			System.out.println("two greater than one");
		
	}

}
